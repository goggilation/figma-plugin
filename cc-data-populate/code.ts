const CreateArrays = async () => {
  mdsVehicles.map((v) => {
    vehicleNames.push(v.vehicleName);
    vinNrs.push(v.vin);
    models.push(v.marketingType);
    customData.map((cd) => {
      regNrs.push(cd.regNr);
      streets.push(cd.position);
    });
  });
  mdsDrivers.map((d) => {
    if (d.firstName.indexOf("Unknown") != 0)
      driverNames.push(`${d.firstName} ${d.lastName}`);
  });
};

// Load font via async function
async function myFontLoadingFunction(textLayers) {
  console.log(`Layers sent to load font`);
  console.log("---");
  console.log("Awaiting the fonts.");

  await textLayers.map(async (t) => {
    if (loadedFonts.indexOf(t.fontName.style) === -1) {
      console.log("Loading custom font");
      console.log(t.fontName);

      loadedFonts.push(t.fontName.style);
      await figma.loadFontAsync(t.fontName);
    } else {
      console.log(`${t.fontName.family} ${t.fontName.style} already loaded`);
    }
  });
  console.log("Loading standard font");
  await figma.loadFontAsync({ family: "Roboto", style: "Regular" });
  return true;
}

const CheckForNonTextObjects = async (selection) => {
  // Check for non text objects and throw an error if there are any. Otherwise put selections into a new array
  selection.map((s) => {
    if (s.type != "TEXT") {
      throw "All selected fields are not text objects";
    } else selectionArray = selection;
  });
  return true;
};

const LoopAndChangeText = async (selection: Array<any>, arrayType: string) => {
  let textArray = [];
  switch (arrayType) {
    case "vehicles":
      textArray = vehicleNames;
      break;
    case "users":
      textArray = driverNames;
      break;
    case "vin":
      textArray = vinNrs;
      break;
    case "model":
      textArray = models;
      break;
    case "services":
      textArray = services;
      break;
    case "street-position":
      textArray = streets;
      break;
    case "reg-numbers":
      textArray = regNrs;
      break;
    default:
      textArray = defArray;
  }
  selection.map(async (item) => {
    const random = Math.floor(Math.random() * textArray.length);
    item.characters = textArray[random];
  });
};

// Calls to "parent.postMessage" from within the HTML page will trigger this function.
// Set to async because of fontloading required.
figma.ui.onmessage = async (msg) => {
  const currentSelection = figma.currentPage.selection;

  // Checking if there are any objects in the selection that aren't TEXT objects.
  await CheckForNonTextObjects(currentSelection);
  await myFontLoadingFunction(currentSelection)
    .then(() => {
      CreateArrays();
      LoopAndChangeText(selectionArray, msg.type);
    })
    .then(() => figma.closePlugin("done"));
};
